#? OOP – Standard way of writing code

'''
Everything in Python is an object.  
Every object has:
    - attributes (data)
    - behaviour (functions/methods)

Example:
    Person           → object
    name, age        → attributes (variables)
    singing, driving → behaviour (methods)

Example: (Imagine 100 houses build by different architects using same bluePrint)
    HouseBluePrint  → class
    All Houses      → objects (These are just the instance of Class)

Attributes  → variables  
Behaviour   → functions (inside a class, they are called methods)

A class defines a blueprint or template for objects.
    - It gives default attributes and behaviour.
    - Each object created from a class has its *own* attributes.
    - Objects should not share instance-specific data with others.

Object = combination of data (attributes) + behaviour (methods)

OOP (Object Oriented Programming):
    A programming paradigm where data and functions
    that operate on that data are bundled together inside objects.
'''



